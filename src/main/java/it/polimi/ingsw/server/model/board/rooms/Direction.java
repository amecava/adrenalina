package it.polimi.ingsw.server.model.board.rooms;

/**
 * Enumeration for the four cardinal directions.
 */
public enum Direction {

    /**
     * In a Cartesian plane, increasing y.
     */
    NORTH,
    /**
     * In a Cartesian plane, increasing x.
     */
    EAST,
    /**
     * In a Cartesian plane, decreasing y.
     */
    SOUTH,
    /**
     * In a Cartesian plane, decreasing x.
     */
    WEST
}