package it.polimi.ingsw.server.model.players.bridges;

/**
 * class that expresses the different adrenalin states
 */
public enum Adrenalin {
    /**
     * normale state adrenalin
     */
    NORMAL,
    /**
     * first state adrenalin
     */
    FIRSTADRENALIN,
    /**
     * second state adrenalin
     */
    SECONDADRENALIN,
    /**
     * first  state frenzy mode
     */
    FIRSTFRENZY,
    /**
     * second state frenzy mode
     */
    SECONDFRENZY
}
